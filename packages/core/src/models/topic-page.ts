// Shared helpers (used by AreaMemoryService, TopicMemoryService, CLAUDE.md
// generator, and skill context-bundle assembly). Factored here so the same
// truncation rules apply everywhere.
// ---------------------------------------------------------------------------

/**
 * First non-empty line of `Current state`, truncated to ~120 chars with an
 * ellipsis. Used for:
 *   - Area file Topics index line
 *   - CLAUDE.md Active Topics block
 *   - `arete topic list` output
 * Returns empty string if Current state is missing/empty.
 */
export function getTopicHeadline(page: TopicPage, maxChars = 120): string {
  const current = page.sections['Current state'];
  if (current === undefined) return '';
  const firstLine = current.split('\n').find((l) => l.trim().length > 0);
  if (firstLine === undefined) return '';
  const trimmed = firstLine.trim();
  if (trimmed.length <= maxChars) return trimmed;
  // Truncate on a word boundary if possible.
  const cutoff = trimmed.slice(0, maxChars);
  const lastSpace = cutoff.lastIndexOf(' ');
  const head = lastSpace > maxChars * 0.7 ? cutoff.slice(0, lastSpace) : cutoff;
  return head.trimEnd() + '…';
}

/**
 * Priority order for section inclusion when budget is tight.
 * Higher priority sections are included first.
 *
 * `Source trail` and `Change log` are deliberately excluded: they carry
 * provenance/audit data (low information density for skill context
 * injection) and `topic_page_retrieval` callers want synthesized
 * narrative, not a list of meeting references. To include those, callers
 * read the raw topic page directly via `renderTopicPage`.
 */
const SECTION_PRIORITY: SectionName[] = [
  'Current state',
  'Open questions',
  'Scope and behavior',
  'Why/background',
  'Known gaps',
  'Relationships',
  'Rollout/timeline',
];

/**
 * Select sections to fit within a word budget, in priority order.
 * Always includes `Current state` first (even if it alone exceeds budget).
 * Subsequent sections are included only if the running total stays within
 * budget. Returns rendered markdown (just the selected sections, no
 * frontmatter or title).
 *
 * Used by `topic_page_retrieval` pattern to inject topic content into skill
 * context without blowing the 1000-word memory budget in
 * `context_bundle_assembly`.
 */
export function selectSectionsForBudget(
  page: TopicPage,
  budgetWords: number,
): string {
  const selected: string[] = [];
  let wordCount = 0;
  let first = true;

  for (const name of SECTION_PRIORITY) {
    const body = page.sections[name];
    if (body === undefined) continue;
    const trimmed = body.trim();
    if (trimmed.length === 0) continue;

    const sectionWords = countWords(trimmed);

    if (first) {
      // Always include Current state / highest-priority section, even if
      // it alone blows the budget — it's the most valuable signal.
      selected.push(`## ${name}\n\n${trimmed}`);
      wordCount += sectionWords;
      first = false;
      continue;
    }

    if (wordCount + sectionWords > budgetWords) break;
    selected.push(`## ${name}\n\n${trimmed}`);
    wordCount += sectionWords;
  }

  return selected.join('\n\n');
}

function countWords(text: string): number {
  const matches = text.match(/\S+/g);
  return matches === null ? 0 : matches.length;
}

function asEntities(v: unknown): TopicEntities | undefined {
  if (v === null || typeof v !== 'object' || Array.isArray(v)) return undefined;
  const rec = v as Record<string, unknown>;
  const entities: TopicEntities = {};
  const people = asStringArray(rec.people);
  if (people !== undefined) entities.people = people;
  const related = asStringArray(rec.related_topics);
  if (related !== undefined) entities.related_topics = related;
  return hasEntityContent(entities) ? entities : undefined;
}
